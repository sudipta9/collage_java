// 4. display the attributes or data members using class and object

public class attributesBasic {

    // data members
    int id = 10;
    String name = "Sudipta";

    public static void main(String[] args) {
        System.out.println("");
        // creating Object
        attributesBasic obj = new attributesBasic();
        // accessing data members
        System.out.println(obj.id);
        System.out.println(obj.name);
    }
}
